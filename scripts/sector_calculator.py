# ─────────────────────────────────────────────────────────────────────────────
# الدالة الرئيسية (async — تستخدم asyncpg connection)
# ─────────────────────────────────────────────────────────────────────────────

async def compute_sector_candles_from_db(conn) -> Dict[str, Dict]:
    """
    يحسب شمعة OHLCV لكل قطاع والمؤشر العام من آخر بيانات 1m في DB.

    Args:
        conn: asyncpg connection أو pool.acquire() connection

    Returns:
        Dict[sector_symbol → candle_dict] جاهز للحفظ في market_data.ohlcv
    """
    now = datetime.now()
    lookback = now - timedelta(minutes=5)  # آخر 5 دقائق

    # ── جلب آخر شمعة لكل سهم من DB ──────────────────────────────────────────
    sql = """
    SELECT DISTINCT ON (symbol)
        symbol, open, high, low, close, volume, time
    FROM market_data.ohlcv
    WHERE timeframe = '1m'
      AND time >= $1
      AND symbol ~ '^[0-9]{4}$'
    ORDER BY symbol, time DESC;
    """
    try:
        rows = await conn.fetch(sql, lookback)
    except Exception as e:
        logger.error(f'❌ DB query error in compute_sector_candles_from_db: {e}')
        return {}

    if not rows:
        logger.warning(
            f'⚠️ No stock candles found in DB for last 5 minutes '
            f'(lookback={lookback.strftime("%H:%M:%S")})'
        )
        return {}

    logger.debug(f'📊 DB returned {len(rows)} stock candles for sector computation')

    # ── تجميع الأسهم حسب القطاع ─────────────────────────────────────────────
    from collections import defaultdict
    sector_groups: Dict[str, List[dict]] = defaultdict(list)

    for row in rows:
        sym = str(row['symbol'])
        sector = STOCK_TO_SECTOR.get(sym)
        if sector:
            sector_groups[sector].append({
                'symbol': sym,
                'open':   float(row['open']   or 0),
                'high':   float(row['high']   or 0),
                'low':    float(row['low']    or 0),
                'close':  float(row['close']  or 0),
                'volume': int(row['volume']   or 0),
            })

    if not sector_groups:
        logger.warning(
            '⚠️ No stocks matched STOCK_TO_SECTOR map — '
            'DB may have different symbols or sector map needs update'
        )
        return {}

    # ── حساب شمعة لكل قطاع ──────────────────────────────────────────────────
    result: Dict[str, Dict] = {}
    ts = now.replace(second=0, microsecond=0)

    for sector_sym, members in sector_groups.items():
        closes  = [m['close']  for m in members if m['close']  > 0]
        opens   = [m['open']   for m in members if m['open']   > 0]
        highs   = [m['high']   for m in members if m['high']   > 0]
        lows    = [m['low']    for m in members if m['low']    > 0]
        volumes = [m['volume'] for m in members]

        if not closes:
            continue

        total_volume = sum(volumes)
        # VWAP: مرجّح بالحجم
        if total_volume > 0:
            vwap = sum(m['close'] * m['volume'] for m in members) / total_volume
        else:
            vwap = sum(closes) / len(closes)

        open_price = sum(opens) / len(opens) if opens else vwap

        result[sector_sym] = {
            'symbol':        sector_sym,
            'name':          SECTOR_NAMES.get(sector_sym, sector_sym),
            'timestamp':     ts,
            'open':          round(open_price,    4),
            'high':          round(max(highs),    4) if highs else round(vwap, 4),
            'low':           round(min(lows),     4) if lows  else round(vwap, 4),
            'close':         round(vwap,          4),
            'volume':        total_volume,
            'source':        'db_sector_calculator',
            'members_count': len(members),
        }

    # ── حساب TASI (90001) كمتوسط مرجّح لكل الأسهم ──────────────────────────
    all_stocks = [row for row in rows if float(row['close'] or 0) > 0]
    if all_stocks:
        total_vol = sum(int(r['volume'] or 0) for r in all_stocks)
        if total_vol > 0:
            tasi_close = sum(
                float(r['close']) * int(r['volume'] or 0) for r in all_stocks
            ) / total_vol
        else:
            tasi_close = sum(float(r['close']) for r in all_stocks) / len(all_stocks)

        tasi_open   = sum(float(r['open']  or tasi_close) for r in all_stocks) / len(all_stocks)
        tasi_high   = max(float(r['high']  or tasi_close) for r in all_stocks)
        tasi_low    = min(float(r['low']   or tasi_close) for r in all_stocks)
        tasi_volume = sum(int(r['volume']  or 0) for r in all_stocks)

        result['90001'] = {
            'symbol':        '90001',
            'name':          'TASI - المؤشر العام',
            'timestamp':     ts,
            'open':          round(tasi_open,  4),
            'high':          round(tasi_high,  4),
            'low':           round(tasi_low,   4),
            'close':         round(tasi_close, 4),
            'volume':        tasi_volume,
            'source':        'db_sector_calculator',
            'members_count': len(all_stocks),
        }

    tasi_ok = '✅' if '90001' in result else '❌'
    logger.info(
        f'📊 compute_sector_candles_from_db: {len(result)} candles computed '
        f'(TASI={tasi_ok}, sectors={len(result) - (1 if "90001" in result else 0)}, '
        f'from {len(rows)} stocks)'
    )
    return result


